// Функция для поиска подкатегории по ключевым словам
function detectSubcategory(categoryObj, text){
    for(const sub in categoryObj.subcategories){
        const keywords = categoryObj.subcategories[sub].keywords;
        for(const kw of keywords){
            if(text.toLowerCase().includes(kw)) return categoryObj.subcategories[sub];
        }
    }
    // Если не нашли подходящую подкатегорию — берем первую
    return Object.values(categoryObj.subcategories)[0];
}

// Генерация вариантов
function generateOptions(name, situation, emotion){
    const lowerText = situation.toLowerCase();

    // Определяем категорию по ключевым словам
    let categoryName = "общее";
    for(const cat in templates.categories){
        const catKeywords = templates.categories[cat].keywords;
        for(const kw of catKeywords){
            if(lowerText.includes(kw)) { categoryName = cat; break; }
        }
    }

    const categoryObj = templates.categories[categoryName];
    const subcategory = detectSubcategory(categoryObj, lowerText);
    const catTemplates = subcategory[emotion];

    const goodChoice = getRandom(catTemplates.good);
    const badChoices = catTemplates.bad.sort(()=>Math.random()-0.5).slice(0,2);

    const options = [goodChoice,...badChoices].sort(()=>Math.random()-0.5);
    return {options: options.map(o=>o.replace(/{name}/g,name)), good: goodChoice.replace("{name}",name)};
}

// Обработчик формы под твою форму
document.getElementById("storyForm").addEventListener("submit", function(e){
    e.preventDefault();

    const name = document.getElementById("childName").value.trim();
    const situation = document.getElementById("situation").value.trim();
    const emotionInput = document.querySelector('input[name="emotion"]:checked');
    if(!name || !situation || !emotionInput) return;

    const emotion = emotionInput.value;
    const {options, good} = generateOptions(name, situation, emotion);

    const choicesDiv = document.getElementById("choices");
    const finalDiv = document.getElementById("finalStory");
    finalDiv.innerHTML = "";
    choicesDiv.innerHTML = `<h3>Выберите, что может сделать ${name}:</h3>`;

    // Создаём кнопки для всех вариантов
    options.forEach(opt=>{
        const btn = document.createElement("button");
        btn.className = "choice-button";
        btn.textContent = opt;

        btn.addEventListener("click", function(){
            const start = `${name} сталкивается с ситуацией: ${situation}.`;

            let analysis = "";
            if(opt === good){
                analysis = getRandom(templates.endGood).replace("{name}", name);
            } else {
                analysis = getRandom(templates.endBad).replace("{name}", name);
            }

            finalDiv.innerHTML = `<p>${start} ${opt} ${analysis}</p>`;
            choicesDiv.innerHTML = "";
        });

        choicesDiv.appendChild(btn);
    });
});



